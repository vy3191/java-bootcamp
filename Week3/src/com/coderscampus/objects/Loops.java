package com.coderscampus.objects;

public class Loops {
	
	public static void main (String[] args) {
		
		// init; termination condition; increment
		for 
		  (int i = 0; 
				i <= 100; 
				i = i + 1) 
		{
			System.out.print(i + ": ");
			System.out.println(i*i);
		}
		
		System.out.println("Done");
	}
}
